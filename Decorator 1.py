
#Decorators:-

'''
Decorators are one of the most helpful
and powerful tools of Python.

These are used to modify the behavior of
the function.

Decorators provide the flexibility to wrap
another function to expand the working of
wrapped function, without permanently
modifying it.

In Decorators, functions are passed as an
argument into another function and then
called inside the wrapper function.

It is also called meta programming where a
part of the program attempts to change
another part of program at compile time.

'''


